class Patient():
    def __init__(self, id: str = None, name: str = None,
                 surname: str = None, father: str = None,
                 birthday: str = None, phone_number : str = None) -> None:
                 self.id = id
                 self.name = name
                 self.surname = surname
                 self.father = father
                 self.birthday = birthday
                 self.phone_number = phone_number
